print("13 % 4 = ", 13 % 4, "\n")
print("13 % (-4) = ", 13 % (-4), "\n")
print("(-13) % 4 = ", (-13) % 4, "\n")
print("(-13) % (-4) = ", (-13) % (-4), "\n")

print("13.modulo(4) = ", 13.modulo(4), "\n")
print("13.modulo(-4) = ", 13.modulo(-4), "\n")
print("(-13).modulo(4) = ", (-13).modulo(4), "\n")
print("(-13).modulo(-4) = ", (-13).modulo(-4), "\n")
